package org.tarun.learning.tweetnews.trends;

public class HashTag {
  private String tag;
  private long count;

  public String getTag() {
    return this.tag;
  }
  public void setTag(String tag) {
    this.tag = tag;
  }
  public long getCount() {
    return this.count;
  }
  public void setCount(long count) {
    this.count = count;
  }
}
